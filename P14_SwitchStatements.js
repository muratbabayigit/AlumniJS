let dayNumber=54;


switch(dayNumber){
    case 1:
        console.log('Pazartesi');
        break;
    case 2:
        console.log('Salı');
        break;
    case 3:
        console.log('Çarşamba');
        break;
    case 4:
        console.log('Perşembe');
        break;
    case 5:
        console.log('Cuma');
        break;
    case 6:
        console.log('Cumartesi');
        break;
    case 7:
        console.log('Pazar');
        break;
    default:
        console.log('Bir haftada 7 gün vardır. Yanlış giriş yapınız')
}