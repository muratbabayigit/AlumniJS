// Toplama (Addition)
let x = 5; // İlk sayıyı tanımla
let y = 3; // İkinci sayıyı tanımla
let sum = x + y; // İki sayıyı topla ve sonucu 'sum' değişkenine ata
console.log("Toplama:", sum); // Toplama işleminin sonucunu konsola yazdır

// Çıkarma (Subtraction)
let a = 10; // Büyük sayıyı tanımla
let b = 4; // Küçük sayıyı tanımla
let difference = a - b; // İki sayıyı çıkar ve sonucu 'difference' değişkenine ata
console.log("Çıkarma:", difference); // Çıkarma işleminin sonucunu konsola yazdır

// Çarpma (Multiplication)
let p = 7; // İlk çarpanı tanımla
let q = 2; // İkinci çarpanı tanımla
let product = p * q; // İki sayıyı çarp ve sonucu 'product' değişkenine ata
console.log("Çarpma:", product); // Çarpma işleminin sonucunu konsola yazdır

// Bölme (Division)
let m = 20; // Bölüneni tanımla
let n = 5; // Böleni tanımla
let quotient = m / n; // Bir sayıyı diğerine böl ve sonucu 'quotient' değişkenine ata
console.log("Bölme:", quotient); // Bölme işleminin sonucunu konsola yazdır

// Modülüs (Modulus)
let num1 = 10; // Bölüneni tanımla
let num2 = 3; // Böleni tanımla
let remainder = num1 % num2; // İki sayının bölümünden kalanı hesapla ve sonucu 'remainder' değişkenine ata
console.log("Modülüs:", remainder); // Modülüs işleminin sonucunu konsola yazdır

//Üs alma

//1. Math.pow() Fonksiyonu:
// 2 üzeri 3'ü hesapla
var result = Math.pow(2, 3);
console.log(result); // Çıktı: 8

//2. Üs Operatörü (**):
// 2 üzeri 3'ü hesapla
var result = 2 ** 3;
console.log(result); // Çıktı: 8