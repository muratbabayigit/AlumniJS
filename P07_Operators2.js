// Arttırarak değer atama (Increment)
let a = 5; // Bir değişken tanımla ve başlangıç değerini ata
a++; // Değişkenin değerini 1 artır
console.log(a); // Değişkenin artırılmış değerini konsola yazdır

// Azaltarak değer atama (Decrement)
let b = 8; // Bir değişken tanımla ve başlangıç değerini ata
b--; // Değişkenin değerini 1 azalt
console.log(b); // Değişkenin azaltılmış değerini konsola yazdır

// Önce Arttırarak değer atama (Pre-Increment)
let x = 10; // Bir değişken tanımla ve başlangıç değerini ata
let y = ++x; // Değişkenin değerini 1 artır ve yeni değeri 'y' değişkenine ata
console.log(x, y); // Değişkenin artırılmış değerini ve y değişkeninin değerini konsola yazdır

// Önce Azaltarak değer atama (Pre-Decrement)
let p = 15; // Bir değişken tanımla ve başlangıç değerini ata
let q = --p; // Değişkenin değerini 1 azalt ve yeni değeri 'q' değişkenine ata
console.log(p, q); // Değişkenin azaltılmış değerini ve q değişkeninin değerini konsola yazdır


// Çarpma ve Artırma (Multiplication and Increment)
let num1 = 3; // Bir sayı tanımla ve başlangıç değerini ata
let num2 = 4; // Bir sayı tanımla ve başlangıç değerini ata
num1 *= num2; // Bir sayıyı diğerine çarp ve sonucu ilk sayıya ata
console.log("Çarpma ve Artırma:", num1); // İlk sayının çarpılmış ve artırılmış değerini konsola yazdır

// Bölme ve Azaltma (Division and Decrement)
let dividend = 20; // Bölüneni tanımla ve başlangıç değerini ata
let divisor = 5; // Böleni tanımla ve başlangıç değerini ata
dividend /= divisor; // Bir sayıyı diğerine böl ve sonucu ilk sayıya ata
console.log("Bölme ve Azaltma:", dividend); // İlk sayının bölünmüş ve azaltılmış değerini konsola yazdır